    private void txtNombreFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNombreFocusGained
        //clear the textfield on focus if the text is "username"
        if(txtNombre.getText().trim().toLowerCase().equals ("username"))
        {
            txtNombre.setText("");
            txtNombre.setForeground(Color.red);
        }
        //set a yellow border to the jlabel icon
        Border jlabel_icon=BorderFactory.createMatteBorder(1, 1, 1, 1, Color.yellow);
        jLabel4.setBorder(jlabel_icon);
        
    }//GEN-LAST:event_txtNombreFocusGained

    private void JPPasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_JPPasswordFocusGained
        //clear the JPassword on focus if the text is "Password"
        String pass=String.valueOf(JPPassword.getPassword());
        if(pass.trim().equals ("")||
            pass.trim().toLowerCase().equals ("password"))
        {
            JPPassword.setText("");
            JPPassword.setForeground(Color.red);
        }
        //set a yellow border to the jlabel icon
        Border jlabel_icon=BorderFactory.createMatteBorder(1, 1, 1, 1, Color.yellow);
        jLabel3.setBorder(jlabel_icon);
    }//GEN-LAST:event_JPPasswordFocusGained

